local originalPosition, afterPosition = {}, {};
local function applyContainerSelectAnimation(con)
	originalPosition[con] = con.Frame.Position;
	afterPosition[con] = con.Frame.Position + UDim2.new(0,0,0,-7);
	con.Frame.InputBegan:Connect(function(input)
		if input.UserInputType == Enum.UserInputType.MouseMovement then
			con.Frame:TweenPosition(afterPosition[con], Enum.EasingDirection.Out, Enum.EasingStyle.Sine, 0.15, true);
		end
	end)
	con.Frame.InputEnded:Connect(function(input)
		if input.UserInputType == Enum.UserInputType.MouseMovement then
			con.Frame:TweenPosition(originalPosition[con], Enum.EasingDirection.Out, Enum.EasingStyle.Sine, 0.15, true);
		end
	end)
end

local function tweenGradient(gradient)
	
	gradient.Offset = Vector2.new(-2, 0);
	local tween = game:GetService("TweenService"):Create(gradient, TweenInfo.new(2.5, Enum.EasingStyle.Linear, Enum.EasingDirection.InOut), {Offset = Vector2.new(2, 0)});
	tween:Play(); tween.Completed:Wait();
	
end

local function addScript(configs)
	if not configs then
		configs = {
			["scriptName"] = "Script",
			["function"] = "function() end",
			["parent"] = nil,
			["type"] = "normal"
		};
	end

	local mainContainer;
	if configs.type == "gold" then
		mainContainer = FirstX.SavedContainers.GoldGameContainer:Clone();
		
		-- Animate UI Gradient for a shiny effect
		coroutine.wrap(function()
			while wait() do
				-- configs.parent is equal to the scrolling frame, not the "active frame"
				while getActiveFrame() == configs.parent.Parent and wait() do
					--mainContainer.Frame.UIGradient.Enabled = true;
					tweenGradient(mainContainer.Frame.UIGradient);
				end
				--mainContainer.Frame.UIGradient.Enabled = false;
			end
		end)();
	else
		mainContainer = FirstX.SavedContainers.GameContainer:Clone();
	end

	mainContainer.Frame.GameName.Text = configs.scriptName;
	mainContainer.Visible = true;
	mainContainer.Parent = configs.parent;
	applyContainerSelectAnimation(mainContainer);

	-- The reason for a function holder is so that I can get the function value and save it easily
	-- for local lib scripts
	if type(configs["function"]) == "string" then
		local stringHolder = Instance.new("StringValue");
		stringHolder.Value = configs["function"];
		stringHolder.Name = "funcHolder";
		stringHolder.Parent = mainContainer;
	end

	mainContainer.Frame.Trigger.MouseButton1Click:Connect(function()
		
		-- Executed Label animation
		coroutine.wrap(function() 
			mainContainer.Frame.ExecutedLabel:TweenPosition(UDim2.new(0.5,0,1,0), 
			Enum.EasingDirection.Out, Enum.EasingStyle.Sine, 0.4, true);
			wait(2);
			mainContainer.Frame.ExecutedLabel:TweenPosition(UDim2.new(0.5,0,2,0), 
			Enum.EasingDirection.Out, Enum.EasingStyle.Sine, 0.4, true);
		end)();

		-- Run Script
		pcall(function()
			if type(configs["function"]) == "function" then
				configs["function"]();
			else
				loadstring(mainContainer.funcHolder.Value)();
			end
		end)

	end)

end

-- A function for smoothly rotating an instance in increments of 90
-- Mainly made for the navigation 3 bar spinning animation
local function rotateInstanceBy(instance, rotationAngle, delay, isCounterClockwise)
	for i = 1, rotationAngle / 40 do
		local tween = game:GetService("TweenService"):Create(instance, TweenInfo.new(delay or 0.03, Enum.EasingStyle.Linear), {Rotation = instance.Rotation + (isCounterClockwise and -40 or 40)});
		tween:Play();
		tween.Completed:Wait();
	end
end

-- Debounce will start off as false so that the user cannot open the nav menu when hub is still loading
-- This is to avoid bugs

local navDebounce = false;
local function handleNavBtn()
	if navDebounce then
		navDebounce = false;

		local navslidethread = coroutine.create(function()
			if FirstX.NavFrame.Position ~= UDim2.new(0, -125, 0, 34) then
				FirstX.NavFrame:TweenPosition(UDim2.new(0, -125, 0, 34),Enum.EasingDirection.Out, Enum.EasingStyle.Quad, 0.3, true);	-- Hide
				getActiveFrame():TweenPosition(getActiveFrame().Position - UDim2.new(0.27,0,0,0), Enum.EasingDirection.Out, Enum.EasingStyle.Quad, 0.3, true);
			else
				FirstX.NavFrame:TweenPosition(UDim2.new(0, 0, 0, 34),Enum.EasingDirection.Out, Enum.EasingStyle.Quad, 0.4, true);		-- Show
				getActiveFrame():TweenPosition(getActiveFrame().Position + UDim2.new(0.27,0,0,0), Enum.EasingDirection.Out, Enum.EasingStyle.Quad, 0.3, true);
			end
		end)

		coroutine.resume(navslidethread);

		if FirstX.NavButton.Visible then

			rotateInstanceBy(FirstX.NavButton, 180);
			FirstX.NavButton.Visible = false;
			FirstX.CloseNavButton.Rotation = FirstX.NavButton.Rotation;
			FirstX.CloseNavButton.Visible = true;
			rotateInstanceBy(FirstX.CloseNavButton, 200);

		else

			rotateInstanceBy(FirstX.CloseNavButton, 180, nil, true);
			FirstX.NavButton.Visible = true;
			FirstX.NavButton.Rotation = FirstX.CloseNavButton.Rotation;
			FirstX.CloseNavButton.Visible = false;
			rotateInstanceBy(FirstX.NavButton, 200, nil, true);

		end
		
		repeat wait() until coroutine.status(navslidethread) == "dead";
		wait(.2);

		navDebounce = true;
	end
end

